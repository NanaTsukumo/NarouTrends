package constant.narou4j;

/**
 * Narou4jで定義されている最終更新日時指定の文字列表現の定数群<br><br>
 * THIS_WEEK 今週(日曜日の午前0時はじまり)<br>
 * LAST_WEEK 先週<br>
 * SEVEN_DAY 過去7日間(7日前の午前0時はじまり)<br>
 * THIS_MONTH 今月<br>
 * LAST_MONTH 先月
 */
public class Narou4jUpdateAt
{
	/**
	 * 今週(日曜日の午前0時はじまり)
	 */
	public static final String THIS_WEEK = "THIS_WEEK";
	/**
	 * 先週
	 */
	public static final String LAST_WEEK = "LAST_WEEK";
	/**
	 * 過去7日間(7日前の午前0時はじまり)
	 */
	public static final String SEVEN_DAY = "SEVEN_DAY";
	/**
	 * 今月
	 */
	public static final String THIS_MONTH = "THIS_MONTH";
	/**
	 * 先月
	 */
	public static final String LAST_MONTH = "LAST_MONTH";
}
